    private readonly IApiKeyResolver _keyResolver;

    private readonly HttpClient _client;

    private readonly AsyncCacheHelper _memoryCache;

    public LangbaseAgentProvider(IApiKeyResolver keyResolver, AsyncCacheHelper asyncCacheHelper,
        IHttpClientFactory httpClientFactory)
    {
        _keyResolver = keyResolver;
        _memoryCache = asyncCacheHelper;
        _client = httpClientFactory.CreateClient();
        _client.BaseAddress = new Uri("https://api.langbase.com/");
    }

    private void ApplyAuthHeader()
    {
        var key = _keyResolver.Resolve(GetIdentifier());

        if (string.IsNullOrWhiteSpace(key))
            throw new InvalidOperationException($"No {nameof(LangbaseAgent)} API key.");

        _client.DefaultRequestHeaders.Authorization = new AuthenticationHeaderValue("Bearer", key);
    }

    private void ApplyProviderHeader(string? providerId)
    {
        if (string.IsNullOrWhiteSpace(providerId))
            throw new InvalidOperationException($"Invalid model");

        var key = _keyResolver.Resolve(providerId);

        if (string.IsNullOrWhiteSpace(key))
            throw new InvalidOperationException($"No {providerId} API key.");

        _client.DefaultRequestHeaders.Remove("LB-LLM-Key");
        _client.DefaultRequestHeaders.Add("LB-LLM-Key", key);
    }

    public async Task<IEnumerable<Model>> ListModels(CancellationToken cancellationToken = default)
          => await this.ListModels(_keyResolver.Resolve(GetIdentifier()));

    public async Task<ChatCompletion> CompleteChatAsync(ChatCompletionOptions options, CancellationToken cancellationToken = default)
    {
        ApplyAuthHeader();
        ApplyProviderHeader(options.Model.Split(":").FirstOrDefault());

        options.ToolChoice = options.Tools.Any() == true
            ? options.ToolChoice == "none" ? "auto" : options.ToolChoice
            : null;

        options.AdditionalProperties ??= new Dictionary<string, JsonElement>();
        options.AdditionalProperties.Add("input", JsonSerializer.SerializeToElement(options.Messages));
        options.Messages = null!;

        return await this.GetChatCompletion(_client,
             options,
             relativeUrl: "v1/agent/run",
             cancellationToken: cancellationToken);
    }

    public IAsyncEnumerable<ChatCompletionUpdate> CompleteChatStreamingAsync(ChatCompletionOptions options, CancellationToken cancellationToken = default)
    {
        ApplyAuthHeader();
        ApplyProviderHeader(options.Model.Split(":").FirstOrDefault());

        options.ToolChoice = options.Tools.Any() == true
            ? options.ToolChoice == "none" ? "auto" : options.ToolChoice
            : null;

        options.AdditionalProperties ??= new Dictionary<string, JsonElement>();
        options.AdditionalProperties.Add("input", JsonSerializer.SerializeToElement(options.Messages));
        options.Messages = null!;

        return this.GetChatCompletions(_client,
                    options,
                    relativeUrl: "v1/agent/run",
                    cancellationToken: cancellationToken);
    }

